# 17.2 Clacular la diferencia de dos conjuntos
conjunto_1 = {1, 2, 3, 4, 5}
conjunto_2 = {1, 8, 14, 25}
# Mostraremos en pantalla los dos conjuntos
print('A = ', conjunto_1)
print('B = ', conjunto_2)

# Acá vermeos el resultado de todo el conjunto A menos el conjunto B
print('A - B = ', (conjunto_1 - conjunto_2))
# Acá vermeos el resultado de todo el conjunto B menos el conjunto A
print('B - A = ', (conjunto_2 - conjunto_1))
